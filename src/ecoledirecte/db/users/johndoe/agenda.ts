const agenda = (date: string): any[] => {
  const o = [];
  for (let index = 0; index < 5; index++) {
    o.push(events[Math.floor(Math.random()*events.length)]);
  }
  var i = 0;
  var _hours = hours;
  o.forEach(e => {
    e.start_date = e.start_date.replace("%d", date);
    e.end_date = e.end_date.replace("%d", date);
    e.start_date = e.start_date.replace("%h", _hours[i]);
    e.end_date = e.end_date.replace("%h", _hours[i+Math.floor(Math.random()*2)+1 ]);
    i++;
  });

  return o;
};

